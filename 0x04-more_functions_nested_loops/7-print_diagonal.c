#include "main.h"

/**
 * print_diagonal - print a digonal line
 *
 * @n: is the number of time the \ character
 *          should be printed
 *
 * Return: always 0 success
 */

void print_diagonal(int n)
{
	int i, j;

	if (n <= 0)
	{
		_putchar('\n');
	}
	else
	{
		for (i = 0; i < n; i++)
		{
			for (j = 0; j < n; j++)
			{
				if (j == i)
					_putchar('\\');
				else if (j < i)
					_putchar(' ');
			}
			_putchar('\n');
		}
	}
}
